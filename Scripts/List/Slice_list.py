my_list = ['p','r','o','g','r','a','m','i','z']

print(my_list)

# elements 3rd to 5th
print(my_list[2:5])

# elements beginning to 4th
print("The list of values ",my_list[:-5])
print(my_list[-7:-5])
print(my_list[-5:-7])

# elements 6th to end
print(my_list[5:])

# elements beginning to end
print(my_list[:])